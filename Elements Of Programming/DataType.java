public class DataType {
    public static void main(String[] args) {

        // integer data type
        int a, b, c;
        a = 5;
        b = 6;
        c = a + b;
        System.out.println(c);

        // char data type
        char x = 'x';
        System.out.println(x);

        // String data type
        String hello = "Hello World";
        System.out.println(hello);

    }
}
